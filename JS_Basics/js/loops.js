// for loop
console.log("For Loop");

for (let i = 0; i < 10; i++) {
    console.log(`${i} I Love JavaScript`);
}

// while loop
console.log("While Loop");

let i = 1;

while (i <= 10) {
    console.log(i);
    i++;
}

// do-while loop - the code will gonna execute at least once
let num = 5;

do {
    console.log("Fav - Java");
    num++;
} while(num <= 6);